package me.hjoon.java.numberString;

public class CharString {

	public static void main(String[] args) {
		System.out.println("생활코딩");
		// 문자
		System.out.println('생');
		// 문자열
		System.out.println("생활코딩2");
		// 이스케이프
		System.out.println("Brian said \"Welcome programming world\"");
		// 연산자
		System.out.println("Brian"+" is"+" a"+" boy.");
		// 여러줄 표시
		System.out.println("Brian\n is\n a\n boy");
	}
}
