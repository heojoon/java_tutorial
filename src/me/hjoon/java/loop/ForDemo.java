package me.hjoon.java.loop;

public class ForDemo {

	public static void main(String[] args) {
		for (int i = 0; i < 10; i++) {
            System.out.println("Coding Everybody " + i);
        }
		for (int i = 2; i < 100; i=i+2) {
            System.out.println("Coding Everybody " + i);
        }
	}
}

